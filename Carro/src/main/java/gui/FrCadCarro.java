 public void objetoParaCampos(Carro carro){
         edtMarca.setText(carro.getMarca());
         edtModelo.setText(carro.getModelo());
         edtAno.setText(carro.getAno()+ " ");
         edtPreco.setText(carro.getPreco()+ " ");
     }
     
      public Carro camposParaObjeto(){
         Carro c = new Carro();
         c.setMarca(edtMarca.getText().trim());
         c.setModelo(edtModelo.getText().trim());
         
         String precoStr = edtPreco.getText().trim();
         if(!precoStr.isEmpty()){
         int preco = Integer.parseInt(precoStr);
         c.setPreco(preco);
         }
         
         String anoStr = edtAno.getText().trim();
         if(!anoStr.isEmpty()){
         int ano = Integer.parseInt(anoStr);
         c.setAno(ano);
         }
      return c;   
     }
    
    
    public void HabilitarCampos(boolean flag){
         edtMarca.setEnabled(flag);
         edtModelo.setEnabled(flag);
         edtAno.setEnabled(flag);
         edtPreco.setEnabled(flag);
     }
     
     public void LimparCampos(){
         edtMarca.setText(" ");
         edtModelo.setText(" ");
         edtAno.setText(" ");
         edtPreco.setText(" ");
     }
