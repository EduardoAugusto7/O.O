    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        this.marcaAntiga = JOptionPane.showInputDialog("informe a marca do carro.");
         
         Carro carro = this.gerente.buscar(marcaAntiga);
         if(carro == null){
               JOptionPane.showMessageDialog(this, "carro inexistente");
         }else{
             this.gerente.remover(marcaAntiga);
               JOptionPane.showMessageDialog(this, "Exclusao feita com sucesso!");
         }
         String Listagem = this.gerente.toString();
         edtListagem.setText(Listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         Carro novoCarro = this.camposParaObjeto();
    if(this.editando == true)
         this.gerente.atualizarCarros(this.marcaAntiga, novoCarro);
     else     
     this.gerente.adicionar(novoCarro);
     this.LimparCampos();
     this.HabilitarCampos(false);
     this.editando = false;
     
     String Listagem = gerente.toString();
     edtListagem.setText(Listagem);
     
       try {
           gerente.salvarNoArquivo("ListagemCarros.csv");
       } catch (IOException ex) {
           Logger.getLogger(FrCadCarro.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

