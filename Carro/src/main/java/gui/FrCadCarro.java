    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        this.marcaAntiga = JOptionPane.showInputDialog("informe a marca do carro.");
      Carro carroEditando = this.gerente.buscar(marcaAntiga);
      
      if(carroEditando != null){
          this.editando = true;
          this.LimparCampos();
          this.HabilitarCampos(true);
          this.objetoParaCampos(carroEditando);
          
      }else{
          System.out.println("Carro inexistente.");
          JOptionPane.showMessageDialog(this, "Carro inexistente");
      }
    }//GEN-LAST:event_btnEditarActionPerformed

